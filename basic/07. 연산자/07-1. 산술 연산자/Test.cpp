#include <iostream>
using namespace std;

int main() {

    int a = 10;
    int b = 2;

    cout << "a + b : " << (a + b) << "\n"; // a + b : 12
    cout << "a - b : " << (a - b) << "\n"; // a - b : 8
    cout << "a * b : " << (a * b) << "\n"; // a * b : 20
    cout << "a / b : " << (a / b) << "\n"; // a / b : 5
    cout << "a % b : " << (a % b) << "\n"; // a % b : 0
    return 0;
}
