#include <iostream>
using namespace std;

int main() {

    int x = 5;
    int y = 3;

    cout << "0 = False, 1 = True\n";
    cout << (x == y) << "\n";
    cout << (x != y) << "\n";
    cout << (x > y) << "\n";
    cout << (x >= y) << "\n";
    cout << (x < y) << "\n";
    cout << (x <= y) << "\n";
    return 0;
}

/*
 * 0 = False, 1 = True
 * 0
 * 1
 * 1
 * 1
 * 0
 * 0
 */
